class Skeleton:
    def __init__(self, health, damage, weapon, drops):
        self.health = health
        self.damage = damage
        self.weapon = weapon
        self.drops = drops
    def __str__(self):
        return f"{self.health}, {self.damage}, {self.weapon}, {self.drops}"

